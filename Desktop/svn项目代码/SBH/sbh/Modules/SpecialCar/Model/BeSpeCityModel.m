#import "BeSpeCityModel.h"

@implementation BeSpeCityModel
- (id)initWithCityDict:(NSDictionary *)dict
{
    if(self = [super init])
    {
        _cityCode = [dict stringValueForKey:@"citycode" defaultValue:@""];
        _cityLng = [dict stringValueForKey:@"lng" defaultValue:@""];
        _cityName = [dict stringValueForKey:@"cityname" defaultValue:@""];
        _cityLat = [dict stringValueForKey:@"lat" defaultValue:@""];
        _sqcityid = [dict stringValueForKey:@"sqcityid"];
    }
    return self;
}
@end
